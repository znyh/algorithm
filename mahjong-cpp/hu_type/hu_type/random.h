

#include <time.h>

#include <iostream>
#include <algorithm>
#include <map>

using namespace std;

void print(unsigned char cards[], int n)
{
	for (auto i = 0; i < n; i++)
	{
		printf("0x%-2x,", cards[i]);
		if ((i + 1) % 9 == 0)
			cout << endl;
	}
	cout << endl;
}

void toMap(unsigned char cards[], int n)
{
	map<unsigned char, int> m;

	for (int i = 0; i < n; i++)
	{
		m[cards[i]]++;
	}

	for (auto i : m)
	{
		printf("0x%-2x,---> %d\n", i.first, i.second);
	}
}

//随机方法1
void random1(unsigned char cards[], int n)
{
	srand((unsigned)time(0));
	for (int i = 0; i < n; i++)
	{
		int idx = rand() % (n - i) + i;
		std::swap(cards[i], cards[idx]);
	}
}

//随机方法2
void random2(unsigned char cards[], int n)
{
	srand((unsigned)time(0));
	random_shuffle(cards, cards + n);
}

